import { describe, expect, it } from 'vitest';
import { hkdfSha256 } from '../src/index.js';

// RFC 5869 Appendix A.1 Test Case 1
const ikm = Uint8Array.from([
  0x0b, 0x0b, 0x0b, 0x0b, 0x0b, 0x0b, 0x0b, 0x0b, 0x0b, 0x0b, 0x0b, 0x0b, 0x0b, 0x0b,
  0x0b, 0x0b, 0x0b, 0x0b, 0x0b, 0x0b, 0x0b, 0x0b,
]);
const salt = Uint8Array.from([0x00, 0x01, 0x02, 0x03, 0x04, 0x05, 0x06, 0x07, 0x08, 0x09, 0x0a, 0x0b, 0x0c]);
const info = Uint8Array.from([0xf0, 0xf1, 0xf2, 0xf3, 0xf4, 0xf5, 0xf6, 0xf7, 0xf8, 0xf9]);

const expectedOkmHex =
  '3cb25f25faacd57a90434f64d0362f2a' +
  '2d2d0a90cf1a5a4c5db02d56ecc4c5bf' +
  '34007208d5b887185865';

describe('RFC5869 compliance', () => {
  it('matches official test vector', async () => {
    const okm = await hkdfSha256(ikm, { salt, info, length: 42 });
    expect(okm).toBe(expectedOkmHex);
  });
});
